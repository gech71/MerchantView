
'use client';

import * as React from 'react';
import { useForm } from 'react-hook-form';
import { zodResolver } from '@hookform/resolvers/zod';
import * as z from 'zod';

import { Button } from '@/components/ui/button';
import {
  Form,
  FormControl,
  FormField,
  FormItem,
  FormLabel,
  FormMessage,
} from '@/components/ui/form';
import { Input } from '@/components/ui/input';
import { useToast } from '@/hooks/use-toast';
import type { Branch } from '@/types';

const branchFormSchema = z.object({
  name: z.string().min(2, 'Branch name must be at least 2 characters.'),
  code: z.string().min(2, 'Branch code must be at least 2 characters.'),
  address: z.string().min(10, 'Address must be at least 10 characters.'),
  contact: z.string().min(10, 'Contact number must be at least 10 characters.'),
});

type BranchFormValues = z.infer<typeof branchFormSchema>;

export function AddBranchForm({ 
  setOpen, 
  onAddBranch 
}: { 
  setOpen: (open: boolean) => void,
  onAddBranch: (branch: Branch) => void 
}) {
  const { toast } = useToast();
  const form = useForm<BranchFormValues>({
    resolver: zodResolver(branchFormSchema),
    defaultValues: {
      name: '',
      code: '',
      address: '',
      contact: '',
    },
  });

  function onSubmit(data: BranchFormValues) {
    const newBranch: Branch = {
      id: new Date().toISOString(), // In a real app, this would be generated by the server
      ...data,
      status: 'Pending',
    };
    onAddBranch(newBranch);
    toast({
      title: 'Branch Submitted for Approval',
      description: `${data.name} has been successfully submitted for verification.`,
    });
    setOpen(false);
  }

  return (
    <Form {...form}>
      <form onSubmit={form.handleSubmit(onSubmit)} className="space-y-4">
        <FormField
          control={form.control}
          name="name"
          render={({ field }) => (
            <FormItem>
              <FormLabel>Branch Name</FormLabel>
              <FormControl>
                <Input placeholder="Downtown Branch" {...field} />
              </FormControl>
              <FormMessage />
            </FormItem>
          )}
        />
        <FormField
          control={form.control}
          name="code"
          render={({ field }) => (
            <FormItem>
              <FormLabel>Branch Code</FormLabel>
              <FormControl>
                <Input placeholder="DT001" {...field} />
              </FormControl>
              <FormMessage />
            </FormItem>
          )}
        />
         <FormField
          control={form.control}
          name="address"
          render={({ field }) => (
            <FormItem>
              <FormLabel>Address</FormLabel>
              <FormControl>
                <Input placeholder="123 Main St, Anytown, USA" {...field} />
              </FormControl>
              <FormMessage />
            </FormItem>
          )}
        />
         <FormField
          control={form.control}
          name="contact"
          render={({ field }) => (
            <FormItem>
              <FormLabel>Contact</FormLabel>
              <FormControl>
                <Input placeholder="555-1234" {...field} />
              </FormControl>
              <FormMessage />
            </FormItem>
          )}
        />
        <div className="flex justify-end gap-2 pt-4">
            <Button type="button" variant="outline" onClick={() => setOpen(false)}>Cancel</Button>
            <Button type="submit">Add Branch</Button>
        </div>
      </form>
    </Form>
  );
}
